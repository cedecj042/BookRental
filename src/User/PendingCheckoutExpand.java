    public PendingCheckoutExpand(List<String> checkoutID, Executable executable) {
        initComponents();
        this.executable = executable;
        rdb = new RentalDB();
        ucdb = new UserCartDB();
        cdb = new CheckoutDB();
        tdb = new TransactionDB();
        bdb = new BookDB();
        ddb = new DeliveryDB();

        this.booksize = checkoutID.size();
        this.checkoutid = checkoutID;
        for (String id : checkoutID) {
            Checkout checkout = cdb.getCheckout(id, UserSession.getInstance().getUser().getUserID());
            Book book = bdb.getBook(checkout.getBookID());
            DeliveryAddress da = ddb.getAddress(checkout.getAddressID());
            checkouts.add(checkout);
            totalinitialamount += checkout.getTotalAmount();
            libraryfee = checkout.getLibraryFee();
            deliveryfee = checkout.getDeliveryFee();
            if (checkout.getAddressID() != 0) {
                deliveryMethod = "Delivery";
                deliveryaddress = da.getAddress();
                deliverylandmark = da.getLandmark();
            } else {
                deliveryMethod = "Pickup";
                deliveryaddress = "City Hall Lapu-Lapu City Cebu";
                deliverylandmark = "Library City Hall public";
            }
            PendingBooksPanel pbpanel = new PendingBooksPanel(book, checkout.getDays(), checkout.getTotalAmount(),id);
            pbp.add(pbpanel);
        }

        //setting up delivery address
        method.setText(deliveryMethod);
        address.setText(deliveryaddress);
        landmark.setText(deliverylandmark);

        //setting up order details 
        totalBooks.setText(Integer.toString(checkoutID.size()));
        libraryFee.setText(Double.toString(libraryfee) + "0");
        deliveryFee.setText(Double.toString(deliveryfee) + "0");
        initialAmount.setText(Double.toString(totalinitialamount) + "0");
        this.sum = totalinitialamount + libraryfee + deliveryfee;
        totalAmount.setText(Double.toString(sum) + "0");
        int num = loadBooks(bookslist, pbp);
        bookslist.setPreferredSize(new Dimension(510, calculatePanelHeight(num)));
    }

    public int calculatePanelHeight(int numBooks) {
        int height = numBooks * 105;
        return height;

    }

    private int loadBooks(Container container, ArrayList<PendingBooksPanel> p) {
        int count = 0;
        container.removeAll();
        for (int i = 0; i < p.size(); i++) {
            container.add(p.get(i));
            count++;
        }
        return count;
    }

