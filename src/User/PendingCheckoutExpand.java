    private void button1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button1ActionPerformed
        // TODO add your handling code here:
        int max = 3 - rdb.countRentalActive(UserSession.getInstance().getUser().getUserID());
        ArrayList<Checkout> checkouts = new ArrayList<>();
        boolean flag = false;

        for (String id : this.checkoutid) {
            Checkout ck = cdb.getCheckout(id, UserSession.getInstance().getUser().getUserID());
            if (rdb.inActiveRental(UserSession.getInstance().getUser().getUserID(), ck.getBookID())) {
                flag = true;
                break;
            }
        }

        if (max > 0 && this.booksize <= max) {

            if (!flag) {
                String pm = null;
                if (cash.isSelected()) {
                    pm = "Cash";

                    String transactionID = tdb.createTransaction(UserSession.getInstance().getUser().getUserID(), pm, this.sum, this.sum, "Done");
                    cdb.setTransactionCheckout(transactionID, this.checkoutid, "Confirmed", UserSession.getInstance().getUser().getUserID());

                    //create rentals
                    rentals = cdb.getCheckouts(this.checkoutid, UserSession.getInstance().getUser().getUserID());
                    for (Rental rental : rentals) {
                        rdb.createRental(rental);
                        bdb.removeBookInventory(rental.getBookID());
                    }
                    this.executable.executeCurrentRentals();
                    this.executable.executePendingCheckout();
                    this.executable.executeCheckoutHistory();
                    GlassPanePopup.closePopupLast();
                    GlassPanePopup.showPopup(new CheckoutSummary(this.checkoutid, transactionID));
                } else if (gcash.isSelected()) {
                    pm = "Gcash";
                    PaymentLogin pl = new PaymentLogin(pm, this.checkoutid, this.sum);
                    pl.setExecutable(this.executable);
                    GlassPanePopup.showPopup(pl);
                }
            } else {
                JOptionPane.showMessageDialog(null, "One of the books is currently rented.", "Already Rented", JOptionPane.INFORMATION_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "You have reached the maximum renting of books.", "Maximum Rented", JOptionPane.INFORMATION_MESSAGE);
        }


    }//GEN-LAST:event_button1ActionPerformed

